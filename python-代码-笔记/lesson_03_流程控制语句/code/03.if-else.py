# if-else语句
# 语法： 
#   if 条件表达式 :
#       代码块
#   else :
#       代码块
# 执行流程：
#   if-else语句在执行时，先对if后的条件表达式进行求值判断
#       如果为True，则执行if后的代码块
#       如果为False，则执行else后的代码块

age = 7
if age > 17 :
    print('你已经成年了~~')
else :
    print('你还未成年~~')